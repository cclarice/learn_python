# Цикл for
#
# Цикл выполняется для каждого элемента
# Синтаксис:
#	for <Название переменной> in <Первое значение>, <Второе>, <Третье>:
#		<Что выполнять>() # или
#		<Что выполнять>(<Название переменной>)

for countdown in 5, 4, 3, 2, 1, "hey!":	# Для каждого элемента вызвать
	print(countdown)					# Вывести на экран текущий элемент coundown'а